//record attendance:
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String studentID = jTextField1.getText();
        String course = jTextField5.getText();
        String presence = jComboBox1.getSelectedItem().toString();
        if (studentID.equals("") || course.equals("") || presence.equals("")) {
            jLabel7.setText("Please enter all details");
        } else {
            Attendance attendance = new Attendance(studentID, course, presence);
            attendanceHandler.recordAttendance(attendance);
            jLabel3.setText("Attendance of " + studentID + ", " + presence + " success");
            jTextField1.setText("");
            jTextField5.setText("");
            jLabel7.setText("");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

