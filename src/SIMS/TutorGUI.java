    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String studentID = jTextField2.getText();
        String course = jTextField4.getText();
        String grade = jTextField3.getText();
        if (studentID.equals("") || course.equals("") || grade.equals("")) {
            jLabel6.setText("Please enter all details");
        } else {
            Progress progress = new Progress(studentID, course, grade);
            progressHandler.recordProgress(progress);
            jLabel6.setText("Progress of " + studentID + ", " + grade + " success");
            jTextField2.setText("");
            jTextField4.setText("");
            jTextField3.setText("");
        }
    }//GEN-LAST:event_jButton6ActionPerformed
